package Basic_dsaWithJava;

public class Question_3 {
    public static void main(String[] args) {
        //Q.Create a Java program that performs arithmetic operations (addition, subtraction, multiplication,
        //division) on integers, floats, and doubles. Print the results.

                // Integers
                int a = 10;
                int b = 2;
                System.out.println("Integer Operations:");
                System.out.println("Addition: " + (a + b));
                System.out.println("Subtraction: " + (a - b));
                System.out.println("Multiplication: " + (a * b));
                System.out.println("Division: " + (a / b));

                // Floats
                float c = 10.5f;
                float d = 2.5f;
                System.out.println("\nFloat Operations:");
                System.out.println("Addition: " + (c + d));
                System.out.println("Subtraction: " + (c - d));
                System.out.println("Multiplication: " + (c * d));
                System.out.println("Division: " + (c / d));

                // Doubles
                double e = 10.5;
                double f = 2.5;
                System.out.println("\nDouble Operations:");
                System.out.println("Addition: " + (e + f));
                System.out.println("Subtraction: " + (e - f));
                System.out.println("Multiplication: " + (e * f));
                System.out.println("Division: " + (e / f));
            }
        }


