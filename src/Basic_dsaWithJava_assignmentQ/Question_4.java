package Basic_dsaWithJava_assignmentQ;

public class Question_4 {
    public static void main(String[] args) {
        // Q.Write a Java program that demonstrates the use of increment (++), decrement (--), and modulus (%)
        //operators. Print the results.

                // Initialize variables
                int a = 10;
                int b = 5;

                // Increment operator
                System.out.println("Initial value of a: " + a);
                a++;
                System.out.println("Value of a after increment: " + a);

                // Decrement operator
                System.out.println("\nInitial value of b: " + b);
                b--;
                System.out.println("Value of b after decrement: " + b);

                // Modulus operator
                int c = a % b;
                System.out.println("\nModulus of a and b: " + c);
            }
        }


